	/**
	 * 
	 * @des:获取当地时间的标准时间
	 * @data:2015年12月2日下午8:16:05
	 * @author:chenleilei
	 */
	public static Date DateToUTCDate(Date date){
		if(date != null){
			Calendar cal =Calendar.getInstance();
			cal.setTimeInMillis(date.getTime());
			//取得时间偏移量
			int zoneOffset = cal.get(Calendar.ZONE_OFFSET);
			//取得夏令时差
			int dstOffset = cal.get(Calendar.DST_OFFSET);
			//从本地时间里扣除这些差量，即可以取得UTC时间：
			cal.add(Calendar.MILLISECOND, -(zoneOffset + dstOffset));
			return cal.getTime();
		}
		return null;
	}
	/**
	 * 
	 * @des:求两个时间段的交集
	 * @parameter：start_time1
	 * @parameter：end_time1
	 * @parameter：start_time2
	 * @parameter：end_time2
	 * @return:Map<String,Date>
	 *
	 */
	public static Map<String,Date> getIntersectionTime(Date start_time1,Date end_time1,Date start_time2,Date end_time2){
		 Map<String,Date> stage = new HashMap<String,Date>();
		 if(start_time1 == null){
			  start_time1 = new Date(0);
		  }
		  if(start_time2 == null){
			  start_time2 = new Date(0);
		  }
		  if(end_time1 == null){
			  end_time1 = new Date();
		  }
		  if(end_time2 == null){
			  end_time2 = new Date();
		  }
		  //无交集
		  if(start_time1.getTime()>end_time2.getTime()){
			  return null;
		  }else if(end_time1.getTime()<start_time2.getTime()){//无交集
			  return null;
		  }else{
			  if(start_time1.getTime()<start_time2.getTime()){
				  start_time1 = start_time2; 
			  }
			  if(end_time1.getTime()>end_time2.getTime()){
				  end_time1 = end_time2;
			  }
				  
			  stage.put("start_time", start_time1);
			  stage.put("end_time", end_time1);
		  }
		return stage;
	}
	
